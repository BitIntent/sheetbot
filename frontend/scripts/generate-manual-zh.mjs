/**
 * ===================================
 * 站内手册生成脚本（中文）
 * - 读取仓库根目录 docs/manual/zh/*.md
 * - 生成前端可直接 import 的数据文件
 * ===================================
 *
 * 设计原则：
 * - 单一事实来源：docs/manual/zh
 * - 前端仅消费生成产物：src/generated/manual-zh.js
 */
import fs from 'node:fs'
import path from 'node:path'

const repoRoot = path.resolve(process.cwd(), '..')
const sourceDir = path.join(repoRoot, 'docs', 'manual', 'zh')
const imgSourceDir = path.join(repoRoot, 'docs', 'img')
const outDir = path.join(process.cwd(), 'src', 'generated')
const outFile = path.join(outDir, 'manual-zh.js')
const publicDocsImgDir = path.join(process.cwd(), 'public', 'docs', 'img')

function mustRead(filePath) {
  return fs.readFileSync(filePath, 'utf-8')
}

function listMarkdownFiles(dir) {
  return fs.readdirSync(dir)
    .filter(name => name.toLowerCase().endsWith('.md'))
    .sort((a, b) => a.localeCompare(b, 'zh-Hans-CN'))
}

function buildSections() {
  const files = listMarkdownFiles(sourceDir)
  const sections = files.map((filename) => {
    const abs = path.join(sourceDir, filename)
    const content = mustRead(abs)
    // 章节 id：用文件名去掉扩展名，保持稳定
    const id = filename.replace(/\.md$/i, '')
    // 标题：优先取首个 H1；否则用文件名
    const m = content.match(/^\s*#\s+(.+)\s*$/m)
    const title = m?.[1]?.trim() || id
    return { id, title, filename, content }
  })
  return sections
}

function writeOutput(sections) {
  fs.mkdirSync(outDir, { recursive: true })
  const payload = JSON.stringify({ generatedAt: new Date().toISOString(), sections }, null, 2)
  const code = `// AUTO-GENERATED FILE. DO NOT EDIT.\n// Generated by scripts/generate-manual-zh.mjs\n\nexport const manualZh = ${payload}\n`
  fs.writeFileSync(outFile, code, 'utf-8')
}

function copyDirRecursive(srcDir, dstDir) {
  if (!fs.existsSync(srcDir)) return
  fs.mkdirSync(dstDir, { recursive: true })
  for (const name of fs.readdirSync(srcDir)) {
    const src = path.join(srcDir, name)
    const dst = path.join(dstDir, name)
    const stat = fs.statSync(src)
    if (stat.isDirectory()) {
      copyDirRecursive(src, dst)
      continue
    }
    fs.copyFileSync(src, dst)
  }
}

function main() {
  if (!fs.existsSync(sourceDir)) {
    console.error(`[manual] sourceDir not found: ${sourceDir}`)
    process.exit(1)
  }
  const sections = buildSections()
  writeOutput(sections)
  copyDirRecursive(imgSourceDir, publicDocsImgDir)
  console.log(`[manual] generated: ${outFile} (${sections.length} sections)`)
}

main()

